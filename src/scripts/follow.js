let butfollow = document.querySelector('.follow')
let following = document.querySelector('.follow')
butfollow.addEventListener('click',function(){
    following.style.background = '#4263EB';
    following.style.color = '#ffffff';
    following.style.border= 'none';
    following.style.width = '89px';  
    following.style.borderRadius = "4px"
    following.innerText = "Seguindo"
    following.style.transform = 'scale(1.1)';
    following.style.transition = 'all 0.5s'
 
})
